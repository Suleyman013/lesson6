public abstract class Human {
    public String name;
    public String surname;
    public int age;
    public String gender;
    public String phonenumber;
    public Human(String name, String surname, int age, String gender, String phonenumber) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.gender = gender;
        this.phonenumber = phonenumber;
    }
}
